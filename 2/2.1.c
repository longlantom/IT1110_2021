#include<stdio.h>

int main(){
    float a, b, tong; //khai bao bien kieu float
    scanf("%f", &a); //nhap so a
    scanf("%f", &b); //nhao so b
    tong = a + b; //cong hai so
    printf("%f", tong); //in bien tong
    return 0;
}
